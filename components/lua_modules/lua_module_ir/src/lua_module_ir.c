/*
 * Walk the board manager descriptor list and verify that the auto-generated
 * config for `device_name` has the layout `lua_ir_board_cfg_t` expects.
 * Returns ESP_ERR_NOT_FOUND if the board doesn't declare the device, and
 * ESP_ERR_INVALID_SIZE if the schema diverges from this module's mirror.
 */
static esp_err_t lua_ir_resolve_board_cfg(const char *device_name,
                                          const lua_ir_board_cfg_t **out)
{
    extern const esp_board_device_desc_t g_esp_board_devices[];
    const esp_board_device_desc_t *desc = g_esp_board_devices;
    while (desc != NULL && desc->name != NULL) {
        if (strcmp(desc->name, device_name) == 0) {
            if (desc->cfg == NULL) {
                return ESP_ERR_NOT_FOUND;
            }
            if (desc->cfg_size != sizeof(lua_ir_board_cfg_t)) {
                ESP_LOGE(TAG,
                         "Board device '%s' cfg_size=%u differs from expected %u; "
                         "board_devices.yaml schema is out of sync with lua_ir_board_cfg_t. "
                         "Every field listed in lua_ir_board_cfg_t MUST be present in YAML "
                         "(use -1 for unused GPIOs).",
                         device_name,
                         (unsigned)desc->cfg_size,
                         (unsigned)sizeof(lua_ir_board_cfg_t));
                return ESP_ERR_INVALID_SIZE;
            }
            *out = (const lua_ir_board_cfg_t *)desc->cfg;
            return ESP_OK;
        }
        desc = desc->next;
    }
    return ESP_ERR_NOT_FOUND;
}

static esp_err_t lua_module_ir_load_board_defaults(const char *device_name,
                                                   lua_ir_resolved_cfg_t *out)
{
    const lua_ir_board_cfg_t *board = NULL;
    esp_err_t err = lua_ir_resolve_board_cfg(device_name, &board);
    if (err != ESP_OK) {
        return err;
    }

    if (board->tx_gpio_num >= 0) {
        out->tx_gpio_num = board->tx_gpio_num;
    }
    if (board->rx_gpio_num >= 0) {
        out->rx_gpio_num = board->rx_gpio_num;
    }
    if (board->ctrl_gpio_num >= 0) {
        out->ctrl_gpio_num = board->ctrl_gpio_num;
        out->ctrl_active_level = board->ctrl_active_level;
    }
    if (board->carrier_hz > 0) {
        out->carrier_hz = (uint32_t)board->carrier_hz;
    }

    return ESP_OK;
}

static void lua_ir_apply_lua_overrides(lua_State *L, int opts_idx,
                                       lua_ir_resolved_cfg_t *cfg)
{
    if (opts_idx == 0 || lua_type(L, opts_idx) != LUA_TTABLE) {
        return;
    }

#define APPLY_INT(field, key) do { \
    lua_getfield(L, opts_idx, key); \
    if (lua_isnumber(L, -1)) { (cfg)->field = (int)lua_tointeger(L, -1); } \
    lua_pop(L, 1); \
} while (0)

#define APPLY_U32(field, key) do { \
    lua_getfield(L, opts_idx, key); \
    if (lua_isnumber(L, -1)) { (cfg)->field = (uint32_t)lua_tointeger(L, -1); } \
    lua_pop(L, 1); \
} while (0)

    APPLY_INT(tx_gpio_num,       "tx_gpio");
    APPLY_INT(rx_gpio_num,       "rx_gpio");
    APPLY_INT(ctrl_gpio_num,     "ctrl_gpio");
    APPLY_INT(ctrl_active_level, "ctrl_active_level");
    APPLY_U32(carrier_hz,        "carrier_hz");
    APPLY_U32(tx_resolution_hz,  "tx_resolution_hz");
    APPLY_U32(rx_resolution_hz,  "rx_resolution_hz");
    APPLY_INT(rx_max_symbols,    "rx_max_symbols");

#undef APPLY_INT
#undef APPLY_U32
}

