static int lua_ir_build_nec_symbols(rmt_symbol_word_t *out, uint16_t address, uint16_t command)
{
    int n = 0;
    out[n++] = (rmt_symbol_word_t){
        .level0 = 1, .duration0 = LUA_IR_NEC_LEADING_HIGH_US,
        .level1 = 0, .duration1 = LUA_IR_NEC_LEADING_LOW_US,
    };
    uint32_t payload = ((uint32_t)address & 0xFFFF) | ((uint32_t)command << 16);
    for (int i = 0; i < 32; i++) {
        bool one = (payload >> i) & 0x1;
        out[n++] = (rmt_symbol_word_t){
            .level0 = 1, .duration0 = LUA_IR_NEC_BIT_HIGH_US,
            .level1 = 0, .duration1 = one ? LUA_IR_NEC_ONE_LOW_US : LUA_IR_NEC_ZERO_LOW_US,
        };
    }
    out[n++] = (rmt_symbol_word_t){
        .level0 = 1, .duration0 = LUA_IR_NEC_ENDING_HIGH_US,
        .level1 = 0, .duration1 = 0,
    };
    return n;
}

