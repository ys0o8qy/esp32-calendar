static int lua_module_ir_close_impl(lua_State *L, lua_module_ir_ud_t *ud)
{
    (void)L;
    if (ud->handle) {
        lua_module_ir_destroy_handle(ud->handle);
        ud->handle = NULL;
    }
    ud->device_name[0] = '\0';
    return 0;
}

static int lua_module_ir_gc(lua_State *L)
{
    lua_module_ir_ud_t *ud =
        (lua_module_ir_ud_t *)luaL_testudata(L, 1, LUA_MODULE_IR_METATABLE);
    if (ud && ud->handle) {
        return lua_module_ir_close_impl(L, ud);
    }
    return 0;
}

static int lua_module_ir_close(lua_State *L)
{
    lua_module_ir_ud_t *ud =
        (lua_module_ir_ud_t *)luaL_checkudata(L, 1, LUA_MODULE_IR_METATABLE);
    if (ud->handle) {
        return lua_module_ir_close_impl(L, ud);
    }
    return 0;
}

static int lua_module_ir_name(lua_State *L)
{
    lua_module_ir_ud_t *ud = lua_module_ir_get_ud(L, 1);
    lua_pushstring(L, ud->device_name);
    return 1;
}

static int lua_module_ir_send_symbols_internal(lua_State *L,
                                               lua_module_ir_handle_t *h,
                                               const rmt_symbol_word_t *symbols,
                                               size_t symbol_count)
{
    esp_err_t err = lua_module_ir_ensure_tx(h);
    if (err != ESP_OK) {
        return luaL_error(L, "ir: TX init failed: %s", esp_err_to_name(err));
    }

    rmt_transmit_config_t tx_cfg = { .loop_count = 0 };
    err = rmt_transmit(h->tx_channel, h->encoder, symbols,
                       symbol_count * sizeof(rmt_symbol_word_t), &tx_cfg);
    if (err != ESP_OK) {
        return luaL_error(L, "ir: rmt_transmit failed: %s", esp_err_to_name(err));
    }
    err = rmt_tx_wait_all_done(h->tx_channel, 5000);
    if (err != ESP_OK) {
        return luaL_error(L, "ir: tx wait failed: %s", esp_err_to_name(err));
    }
    return 0;
}

static int lua_module_ir_send_nec(lua_State *L)
{
    lua_module_ir_ud_t *ud = lua_module_ir_get_ud(L, 1);
    uint16_t address = (uint16_t)luaL_checkinteger(L, 2);
    uint16_t command = (uint16_t)luaL_checkinteger(L, 3);

    if (ud->handle->tx_gpio_num < 0) {
        return luaL_error(L, "ir: TX GPIO not configured");
    }

    rmt_symbol_word_t symbols[34];
    int n = lua_ir_build_nec_symbols(symbols, address, command);
    (void)lua_module_ir_send_symbols_internal(L, ud->handle, symbols, (size_t)n);
    lua_pushboolean(L, 1);
    return 1;
}

static int lua_module_ir_send_raw(lua_State *L)
{
    lua_module_ir_ud_t *ud = lua_module_ir_get_ud(L, 1);
    luaL_checktype(L, 2, LUA_TTABLE);

    if (ud->handle->tx_gpio_num < 0) {
        return luaL_error(L, "ir: TX GPIO not configured");
    }

#if LUA_VERSION_NUM >= 502
    lua_Integer count = luaL_len(L, 2);
#else
    lua_Integer count = lua_objlen(L, 2);
#endif
    if (count <= 0) {
        return luaL_error(L, "ir.send_raw: empty symbol list");
    }

    rmt_symbol_word_t *symbols = calloc((size_t)count, sizeof(rmt_symbol_word_t));
    if (symbols == NULL) {
        return luaL_error(L, "ir.send_raw: out of memory");
    }

    for (lua_Integer i = 0; i < count; i++) {
        lua_rawgeti(L, 2, i + 1);
        if (!lua_istable(L, -1)) {
            free(symbols);
            return luaL_error(L, "ir.send_raw: symbol[%d] is not a table", (int)(i + 1));
        }
        lua_getfield(L, -1, "level0");
        symbols[i].level0 = (uint32_t)luaL_optinteger(L, -1, 1) & 0x1;
        lua_pop(L, 1);
        lua_getfield(L, -1, "duration0");
        symbols[i].duration0 = (uint32_t)luaL_optinteger(L, -1, 0) & 0x7FFF;
        lua_pop(L, 1);
        lua_getfield(L, -1, "level1");
        symbols[i].level1 = (uint32_t)luaL_optinteger(L, -1, 0) & 0x1;
        lua_pop(L, 1);
        lua_getfield(L, -1, "duration1");
        symbols[i].duration1 = (uint32_t)luaL_optinteger(L, -1, 0) & 0x7FFF;
        lua_pop(L, 1);
        lua_pop(L, 1);
    }

    int rc = lua_module_ir_send_symbols_internal(L, ud->handle, symbols, (size_t)count);
    free(symbols);
    if (rc != 0) {
        return rc;
    }
    lua_pushboolean(L, 1);
    return 1;
}

static int lua_module_ir_receive(lua_State *L)
{
    lua_module_ir_ud_t *ud = lua_module_ir_get_ud(L, 1);
    int timeout_ms = (int)luaL_optinteger(L, 2, 5000);

    if (ud->handle->rx_gpio_num < 0) {
        return luaL_error(L, "ir: RX GPIO not configured");
    }

    esp_err_t err = lua_module_ir_ensure_rx(ud->handle);
    if (err != ESP_OK) {
        return luaL_error(L, "ir: RX init failed: %s", esp_err_to_name(err));
    }

    rmt_receive_config_t rcv_cfg = {
        .signal_range_min_ns = LUA_MODULE_IR_RX_MIN_NS,
        .signal_range_max_ns = LUA_MODULE_IR_RX_MAX_NS,
    };

    /*
     * Loop until we either receive a frame that contains at least one symbol
     * with non-zero duration, or the user-supplied timeout elapses. This
     * filters out the empty/zero-duration "frames" the RMT driver sometimes
     * emits when the line is briefly disturbed (e.g. right after rmt_enable
     * or by short noise spikes shorter than signal_range_min_ns).
     */
    rmt_rx_done_event_data_t evt;
    TickType_t deadline = xTaskGetTickCount() + pdMS_TO_TICKS(timeout_ms);
    bool got_real_frame = false;
    while (!got_real_frame) {
        err = rmt_receive(ud->handle->rx_channel, ud->handle->rx_buffer,
                          ud->handle->rx_max_symbols * sizeof(rmt_symbol_word_t), &rcv_cfg);
        if (err != ESP_OK) {
            return luaL_error(L, "ir: rmt_receive failed: %s", esp_err_to_name(err));
        }

        TickType_t now = xTaskGetTickCount();
        TickType_t wait = (deadline > now) ? (deadline - now) : 0;
        if (xQueueReceive(ud->handle->rx_queue, &evt, wait) != pdPASS) {
            lua_pushnil(L);
            lua_pushstring(L, "timeout");
            return 2;
        }

        if (evt.num_symbols == 0) {
            continue;
        }
        /*
         * Reject obvious noise bursts: any real IR remote frame lasts at
         * least ~10ms (NEC repeat alone is 13.5ms, regular NEC ~67ms). A
         * stray edge captured right after rmt_enable typically yields a
         * 1-2 symbol "frame" with sub-millisecond total duration.
         */
        uint32_t total_us = 0;
        for (size_t i = 0; i < evt.num_symbols; i++) {
            total_us += evt.received_symbols[i].duration0;
            total_us += evt.received_symbols[i].duration1;
        }
        if (total_us >= 3000) {
            got_real_frame = true;
        } else {
            ESP_LOGD(TAG, "ignored noise burst: %u symbols, %" PRIu32 "us total",
                     (unsigned)evt.num_symbols, total_us);
        }
    }

    lua_newtable(L);
    for (size_t i = 0; i < evt.num_symbols; i++) {
        const rmt_symbol_word_t *s = &evt.received_symbols[i];
        lua_newtable(L);
        lua_pushinteger(L, s->level0 ? 0 : 1);
        lua_setfield(L, -2, "level0");
        lua_pushinteger(L, s->duration0);
        lua_setfield(L, -2, "duration0");
        lua_pushinteger(L, s->level1 ? 0 : 1);
        lua_setfield(L, -2, "level1");
        lua_pushinteger(L, s->duration1);
        lua_setfield(L, -2, "duration1");
        lua_rawseti(L, -2, (lua_Integer)(i + 1));
    }
    return 1;
}

static int lua_module_ir_info(lua_State *L)
{
    lua_module_ir_ud_t *ud = lua_module_ir_get_ud(L, 1);
    lua_module_ir_handle_t *h = ud->handle;

    lua_newtable(L);
    lua_pushstring(L, ud->device_name);
    lua_setfield(L, -2, "name");
    lua_pushinteger(L, h->tx_gpio_num);
    lua_setfield(L, -2, "tx_gpio");
    lua_pushinteger(L, h->rx_gpio_num);
    lua_setfield(L, -2, "rx_gpio");
    lua_pushinteger(L, h->ctrl_gpio_num);
    lua_setfield(L, -2, "ctrl_gpio");
    lua_pushinteger(L, h->carrier_hz);
    lua_setfield(L, -2, "carrier_hz");
    lua_pushinteger(L, h->tx_resolution_hz);
    lua_setfield(L, -2, "tx_resolution_hz");
    lua_pushinteger(L, h->rx_resolution_hz);
    lua_setfield(L, -2, "rx_resolution_hz");
    return 1;
}

static int lua_module_ir_new(lua_State *L)
{
    const char *device_name = LUA_MODULE_IR_DEFAULT_NAME;
    int opts_idx = 0;

    if (lua_isstring(L, 1)) {
        device_name = lua_tostring(L, 1);
        if (lua_istable(L, 2)) {
            opts_idx = 2;
        }
    } else if (lua_istable(L, 1)) {
        opts_idx = 1;
        lua_getfield(L, 1, "device");
        if (lua_isstring(L, -1)) {
            device_name = lua_tostring(L, -1);
        }
        lua_pop(L, 1);
    }

    if (strlen(device_name) >= LUA_MODULE_IR_MAX_NAME_LEN) {
        return luaL_error(L, "ir device name too long");
    }

    lua_ir_resolved_cfg_t cfg = {
        .tx_gpio_num       = -1,
        .rx_gpio_num       = -1,
        .ctrl_gpio_num     = -1,
        .ctrl_active_level = 0,
        .carrier_hz        = CONFIG_LUA_MODULE_IR_DEFAULT_CARRIER_HZ,
        .tx_resolution_hz  = CONFIG_LUA_MODULE_IR_DEFAULT_TX_RESOLUTION_HZ,
        .rx_resolution_hz  = CONFIG_LUA_MODULE_IR_DEFAULT_RX_RESOLUTION_HZ,
        .rx_max_symbols    = CONFIG_LUA_MODULE_IR_RX_MAX_SYMBOLS,
    };

    esp_err_t board_err = lua_module_ir_load_board_defaults(device_name, &cfg);
    if (board_err == ESP_ERR_INVALID_SIZE) {
        return luaL_error(L,
                          "ir.new: board device '%s' config schema mismatch "
                          "(see error log above for details)", device_name);
    }
    lua_ir_apply_lua_overrides(L, opts_idx, &cfg);

    if (cfg.tx_gpio_num < 0 && cfg.rx_gpio_num < 0) {
        return luaL_error(L, "ir.new: missing tx_gpio/rx_gpio for device '%s'", device_name);
    }
    if (cfg.carrier_hz == 0) {
        return luaL_error(L, "ir.new: invalid carrier_hz");
    }
    if (cfg.rx_max_symbols <= 0) {
        cfg.rx_max_symbols = CONFIG_LUA_MODULE_IR_RX_MAX_SYMBOLS;
    }

    lua_module_ir_handle_t *handle = NULL;
    esp_err_t err = lua_module_ir_create_handle(&cfg, &handle);
    if (err != ESP_OK || handle == NULL) {
        return luaL_error(L, "ir.new failed: %s",
                          esp_err_to_name(err != ESP_OK ? err : ESP_FAIL));
    }

    lua_module_ir_ud_t *ud =
        (lua_module_ir_ud_t *)lua_newuserdata(L, sizeof(*ud));
    memset(ud, 0, sizeof(*ud));
    ud->handle = handle;
    snprintf(ud->device_name, sizeof(ud->device_name), "%s", device_name);

    luaL_getmetatable(L, LUA_MODULE_IR_METATABLE);
    lua_setmetatable(L, -2);
    return 1;
}

