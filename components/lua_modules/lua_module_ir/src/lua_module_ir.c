/*
 * NEC protocol timings (microseconds) - leader and bit windows.
 */
#define LUA_IR_NEC_LEADING_HIGH_US   9000
#define LUA_IR_NEC_LEADING_LOW_US    4500
#define LUA_IR_NEC_BIT_HIGH_US       560
#define LUA_IR_NEC_ZERO_LOW_US       560
#define LUA_IR_NEC_ONE_LOW_US        1690
#define LUA_IR_NEC_ENDING_HIGH_US    560

