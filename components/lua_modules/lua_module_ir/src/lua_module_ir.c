int luaopen_ir(lua_State *L)
{
    if (luaL_newmetatable(L, LUA_MODULE_IR_METATABLE)) {
        lua_pushcfunction(L, lua_module_ir_gc);
        lua_setfield(L, -2, "__gc");
        lua_pushvalue(L, -1);
        lua_setfield(L, -2, "__index");
        lua_pushcfunction(L, lua_module_ir_send_nec);
        lua_setfield(L, -2, "send_nec");
        lua_pushcfunction(L, lua_module_ir_send_raw);
        lua_setfield(L, -2, "send_raw");
        lua_pushcfunction(L, lua_module_ir_receive);
        lua_setfield(L, -2, "receive");
        lua_pushcfunction(L, lua_module_ir_info);
        lua_setfield(L, -2, "info");
        lua_pushcfunction(L, lua_module_ir_name);
        lua_setfield(L, -2, "name");
        lua_pushcfunction(L, lua_module_ir_close);
        lua_setfield(L, -2, "close");
    }
    lua_pop(L, 1);

    lua_newtable(L);
    lua_pushcfunction(L, lua_module_ir_new);
    lua_setfield(L, -2, "new");
    return 1;
}

esp_err_t lua_module_ir_register(void)
{
    return cap_lua_register_module("ir", luaopen_ir);
}
